package pe.com.fe.beanxml;

public class DocumentoRelacionado {
	
	String serieNumeroDocRelacionado;
	String tipoDocuRelacionado;
	
	public DocumentoRelacionado() {
		super();
	}

	public String getSerieNumeroDocRelacionado() {
		return serieNumeroDocRelacionado;
	}
	
	public void setSerieNumeroDocRelacionado(String serieNumeroDocRelacionado) {
		this.serieNumeroDocRelacionado = serieNumeroDocRelacionado;
	}
	
	public String getTipoDocuRelacionado() {
		return tipoDocuRelacionado;
	}
	
	public void setTipoDocuRelacionado(String tipoDocuRelacionado) {
		this.tipoDocuRelacionado = tipoDocuRelacionado;
	}
	
}